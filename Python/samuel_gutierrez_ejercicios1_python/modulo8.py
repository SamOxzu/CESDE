# CICLO WHILE (Hace el proceso mientras la condición sea verdadera)
w = 1
while w < 10:
    print("Esta instrucción se ha ejecutado por ",w," vez en el ciclo while")
    w = w+1

# CICLO FOR (Recorre una secuencia, por ejemplo una lista)
comida = ["manzana","galleta","carne","arroz"]
for x in comida:
    print(x)